// Pour creer une variable il existe 3 facon qui sont les suivante
// Let = locale (ne peut pas etre utilisé dans tout le code)
// Var = globale (Peut etre utilisé a tout moment , partout dans le code)
// Const = Constante (Ne pourra jamais etre modifier (voir ligne 21))

// Booleen
let majorite = true;

// Definir le changement d'une variable booleen (exemple : partie gagné)
let win = false;
// on modifie directement la valeur de win en effectuant le code suivant
// Win deviens true
win = true;

// Nombre
let age = 28;

// Chaine de caractere (String)
let prenom = "fabio" ;
let prenom2 = "Kamel";

// Avec Const la valeur ne pourra jamais etre modifier elle sera toujours constante
const prenom3 = "Moi";

// Ici nous devons impérativement nommé le nom des variables differement
// pour leur fonctionnement sinon une erreur se produira. 

// Lextension turbo console log peut etre utilisé pour generé automatiquement les consoles logs
// en pressant sur CTRL + ALT + L. 
console.log(prenom,prenom2,prenom3,majorite);
